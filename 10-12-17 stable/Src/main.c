int main(void)
{

  /* USER CODE BEGIN 1 */


  /* USER CODE END 1 */

  /* Enable I-Cache-------------------------------------------------------------*/
  SCB_EnableICache();

  /* Enable D-Cache-------------------------------------------------------------*/
  SCB_EnableDCache();

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC3_Init();
  MX_DMA2D_Init();
  MX_FMC_Init();
  MX_LTDC_Init();
  MX_UART5_Init();
  MX_I2C3_Init();
  MX_SPI2_Init();

  /* USER CODE BEGIN 2 */
	
	/*
	// output to the Debug viewer using the ITM
	ITM_Port8(0) = 'a';
	while (ITM_Port8(0) == 0);
	ITM_Port8(0) = 0x0D;
	while (ITM_Port8(0) == 0);
	ITM_Port8(0) = 0x0A;
	*/
  /* LCD Initialization */
	BSP_LCD_Init();
	BSP_LCD_LayerDefaultInit(0, LCD_FB_START_ADDRESS);

  /* Enable the LCD */
  BSP_LCD_DisplayOn();

 /* Configure Front layer (0) */
  BSP_LCD_SelectLayer(0);
  /* Clear the Background Layer */
  BSP_LCD_Clear(LCD_COLOR_BLACK);
  //BSP_LCD_SetTransparency(1,0xFF);
	BSP_LCD_SetTextColor(LCD_COLOR_WHITE);
	BSP_LCD_SetBackColor(LCD_COLOR_BLACK);
	
	//Initialise Touch Screen
	BSP_TS_Init(480,272);
	printf("LCD initialisation complete\n");
	
	
  printf("Waiting for user to dismiss splash screen\n");
	printf("Ensure that AFE is connected\n");
	
	//Display splash screen on LCD
	drawsplash();

	/* SPI Peripheral Initialisation */
	/* Set the CS high (disable) for DAC & PGA*/
	HAL_GPIO_WritePin(GPIOI, GPIO_PIN_2, GPIO_PIN_SET);
	HAL_GPIO_WritePin(GPIOI, GPIO_PIN_3, GPIO_PIN_SET);

	//Initialise the DAC and PGA
	dac_set_level(0); //zero offset (Vref = 0 V)
	pga_control(0); //zero gain

	/* Initialise the AFE auto-switching as disabled */
	HAL_GPIO_WritePin(GPIOC, GPIO_PIN_6, GPIO_PIN_SET);
	HAL_GPIO_WritePin(GPIOC, GPIO_PIN_7, GPIO_PIN_SET);
	
	printf("DAC & PGA initialised\n");
	
  /* Configure Background Layer (1) */
	/* Comment out the following line to disable the background
	   - reduces programming time during testing */
  BSP_LCD_LayerRgb565Init(1, (uint32_t)&RGB565_320x240); 
	BSP_LCD_SetTransparency(1, 80);
		
	//Initialise touch screen in interrupt mode
	BSP_TS_ITConfig();
	

	cleartrace(); //clear the trace area
	run_state = 1; //Initially start the DSO with trace running
	startadc3(); //start ADC3
	printf("Init completed; DSO started\n");
	
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

		//update the status on LCD
		drawmain();
		drawstatus();
		
		//if current state = 1, start the DSO
		if (run_state == 1)
		{
			drawrun(); //display DSO running
			startadc3(); //start ADC3
		}
		//otherwise pause the DSO
		else
		{
			drawstop(); //display DSO stopped
			stopadc3();	//stop ADC3
		}	
		HAL_Delay(50); //Delay before reloading the ADC + refreshing status
  }

  /* USER CODE END 3 */

}

